package StringMethods;

public class Concatination {
	public static void main(String[] args) {
		String name = "Nirmal";
		String fullname = name.concat("Kumar");
		System.out.println("The fullname is" +" "+ fullname);
		String name1 = "Sowmiya";
		String fullname1 = name1.concat("Sridhar");
		System.out.println("The fullname1 is" +" "+ fullname1);
		
	}

}
